# 탈출문자 

# \n : 줄바꿈
print("가나다라마바사\n아자차카타하")

# \특수기호 : !@#$%""등 특수기호 사용.
print("저는 \"파이썬\"입니다.")

# \\ : 문장내에서 \ 사용
print("경로 : C:\\VSCode\\Python\\escape.py")

# \r : 커서를 맨앞으로 이동.
print("Red Apple\rPine")
#  Red Apple에서 커서를 맨앞으로 이동하고 덮어쓰기 >> PineApple

# \b : 백스페이스 (지움.)
print("Redd\bApple")

# \t : tab 탭
print("RED\tApple")